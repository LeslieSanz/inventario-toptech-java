
package Clases;

public class Proveedor {
    private int codigoprov;
    private int telefono;
    private String nombreprov;
    private String direccion;

    /**
     * @return the codigoprov
     */
    public int getCodigoprov() {
        return codigoprov;
    }

    /**
     * @param codigoprov the codigoprov to set
     */
    public void setCodigoprov(int codigoprov) {
        this.codigoprov = codigoprov;
    }

    /**
     * @return the telefono
     */
    public int getTelefono() {
        return telefono;
    }

    /**
     * @param telefono the telefono to set
     */
    public void setTelefono(int telefono) {
        this.telefono = telefono;
    }

    /**
     * @return the nombreprov
     */
    public String getNombreprov() {
        return nombreprov;
    }

    /**
     * @param nombreprov the nombreprov to set
     */
    public void setNombreprov(String nombreprov) {
        this.nombreprov = nombreprov;
    }
    /**
     * @return the direccion
     */
    public String getDireccion() {
        return direccion;
    }

    /**
     * @param direccion the direccion to set
     */
    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }
}
