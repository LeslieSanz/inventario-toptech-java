package Clases;


public class Stock {
    private int invinc;
    private int invfinal;
    private int total;
    private String nuevoprod;

    /**
     * @return the invinc
     */
    public int getInvinc() {
        return invinc;
    }

    /**
     * @param invinc the invinc to set
     */
    public void setInvinc(int invinc) {
        this.invinc = invinc;
    }

    /**
     * @return the invfinal
     */
    public int getInvfinal() {
        return invfinal;
    }

    /**
     * @param invfinal the invfinal to set
     */
    public void setInvfinal(int invfinal) {
        this.invfinal = invfinal;
    }

    /**
     * @return the total
     */
    public int getTotal() {
        return total;
    }

    /**
     * @param total the total to set
     */
    public void setTotal(int total) {
        this.total = total;
    }

    /**
     * @return the nuevoprod
     */
    public String getNuevoprod() {
        return nuevoprod;
    }

    /**
     * @param nuevoprod the nuevoprod to set
     */
    public void setNuevoprod(String nuevoprod) {
        this.nuevoprod = nuevoprod;
    }
}
